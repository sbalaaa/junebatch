
public class ForLoopDemoTwo {

	public static void main(String[] args) {
		String[] fruits =  { "Apple","Banana", "Orange"};
		
		//i<=fruits.length-1
		//i<fruits.length
		
		
		 for(int i=0;i<=fruits.length-1;i++) {
			
			 System.out.println("Fruit name is " + fruits[i]);
			
		 }
		 
		 System.out.println("Loop End " );


	}

}
