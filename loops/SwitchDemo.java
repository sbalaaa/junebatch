
public class SwitchDemo {

	public static void main(String[] args) {

		int number = 500;
		
		switch(number) {
			case 100: {
				System.out.println("Number is 100");
				break;
			}
			case 200: {
				System.out.println("Number is 200");
				break;
			}
			case 300: {
				System.out.println("Number is 300");
				break;
			}
			default: {
				System.out.println("Number is not 100,200,300");
			}
		
		}

	}

}
