
public class ForLoopContinueDemo {

	public static void main(String[] args) {

		 
		 System.out.println("Loop Started");
		
	
		 for(int i=1;i<=10;i++) {
			 
			 if( i < 6) {
				 continue;
			 }

			 System.out.println("Print me " + i);

		 }
		 
		// break;
		 
		 System.out.println("Loop End " );


	}

}


	
