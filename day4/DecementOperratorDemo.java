
public class DecementOperratorDemo {

	public static void main(String[] args) {
		int i = 0;
		int j = i--;
		System.out.println(i); // -1
		System.out.println(j); // 0 
		
		System.out.println("\n \n \n");
		
		int k = 0;
		int l = --k;
		System.out.println(k); // -1
		System.out.println(l); // -1

	}

}


