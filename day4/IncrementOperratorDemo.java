
public class IncrementOperratorDemo {

	public static void main(String[] args) {
		int i = 0;
		int j = i++;
		System.out.println(i);
		System.out.println(j);
		
		System.out.println("\n \n \n");
		
		int k = 0;
		int l = ++k;
		System.out.println(k);
		System.out.println(l);

	}

}


