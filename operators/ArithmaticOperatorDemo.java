
public class ArithmaticOperatorDemo {

	public static void main(String[] args) {
		int firstNumber = 24;
		int secondNumber = 10;
		
		int addResult = firstNumber + secondNumber;
		int minusResult = firstNumber - secondNumber;
		int multiplyResult = firstNumber * secondNumber;
		int quationtResult = firstNumber / secondNumber;
		int remainderResult = firstNumber % secondNumber;
		
		
		
		System.out.println(addResult);
		System.out.println(minusResult);
		System.out.println(multiplyResult);
		System.out.println(quationtResult);
		System.out.println(remainderResult);

	}

}
